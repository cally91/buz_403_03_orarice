--SQL을 3가지로 크게 분류를 하면 
--DDL(Data Definition lang):데이터 저장소 ,사용자 생성 제거하는 명령
-- 이 Lang 는 주로 DBA가 사용된다
-- 데이터 물리적인 어떤 상태를 변경(생성삭제)할수 있기 때문에
-- 매우 주의를 해서 사용해야한다
-- # 데이터를 물리적으로 변견한다는것은
-- 어떤 조건에 관계없이 다량의 데이터에 대하여 영황을 미치는것을 의미

--DML (Data Management data Menfuletion Lang
-- 주로 데이터를 추가,조회(읽기), 변경 , 삭제 등 을 수행하여
-- 기복적으로 제한된 개수의 데이터에 변경이 일어난다.
-- 특별히 악의적으로 데이터를 변경하지 않는다면
-- 큰 영황없는 데이터변경이 이루어진다
-- 데이터 추가 :insert into balues
-- 데이터 조회 : SELECT FROM
--데이터 변경 : UPDATE SET WHERE
-- 데이터 삭제 : DELETE FROM WHERE

--DCL (Data Controll Lang)
-- 데이터에 직접적인 어떤영황을 미치지 않지만
-- 데이터의 상태를 변경하거나
-- 사용자의 권한들을 제어하여
-- 데이터를 보호하는 역활들을 수핸한다
--CUD(INSERT UPDATE DELETE)된 데이터를 취소 기능
-- CYD된 데이터를 확정하는 기능
-- CUD를 수행하는 동안에는 변경된 데이터에 대한 정보가
-- 메모리(기억장소)에 있게된다 
--메모리는 특정상 작동되는 동안 전훤등의 이상이 발생하면
-- 데이터를모두 잃게된다
-- 그레서 메모리에 남아있는데이터 변경사향을 실제 물리적저장소에
-- 강제로 반영할 필요가 있을때가 있다
-- 이떄 사용하는 명령어를 데이터확정기능 명령